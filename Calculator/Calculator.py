import math
class Calculator:
    def __init__(self):
        pass

    def addition(self, a, b):
        return a + b

    def subtraction(self, a, b):
        return a - b

    def multiply(self, a, b):
        return a * b

    def divide(self, a, b):
        return a / b

    def squareRoot(self, a):
        return math.sqrt(a)

    def square(self, a):
        return a * a

